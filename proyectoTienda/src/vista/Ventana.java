    private void nDocClienteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nDocClienteKeyTyped
        char caracter = evt.getKeyChar();

      // Verificar si la tecla pulsada no es un digito
      if(((caracter < '0') ||
         (caracter > '9')) &&
         (caracter != '\b' /*corresponde a BACK_SPACE*/))
      {
         evt.consume();  // ignorar el evento de teclado
      }
   
    }//GEN-LAST:event_nDocClienteKeyTyped


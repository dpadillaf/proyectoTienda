    private void btnVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVentaActionPerformed
        try{
            if ( btnVenta.getText() == "Aceptar" ){
                Venta ven = new Venta ();
                
                Cliente cli = tienda.validarCliente( Long.parseLong( nDocClienteVenta.getText() ) );
                if ( cli != null ){
                    ven.setCliente(cli);
                }else{
                    JOptionPane.showMessageDialog(null, "No existe cliente ese no. de Documento", "Error", 
                    JOptionPane.WARNING_MESSAGE);
                    throw new IndexOutOfBoundsException();
                }
                
                ven.setCodVenta( tienda.getVentas().size() + 1 );
                ven.setProductos( venta );
                venta = new ArrayList<Producto>();
                listarVentasProductos();
                tienda.agregarVenta(ven);
                listarVentas();
                limpiarInputsVenta();
                
                Factura fact = new Factura(new javax.swing.JFrame(), true, ven.generarFactura());
                fact.setVisible(true);
            }else{
                venta = new ArrayList<Producto>();
                limpiarInputsVentaProductos();
                limpiarInputsVenta();
                listarVentasProductos();
                btnVenta.setText( "Aceptar" );
                btnVentaBorrar.setVisible(false);
                btnAgregarProductoVenta.setVisible( true );
            }
        }catch( Exception e ){
            JOptionPane.showMessageDialog(null, "Ingrese los datos correctamente", "Error", 
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnVentaActionPerformed


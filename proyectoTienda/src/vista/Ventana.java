    private void btnAgregarProductoVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoVentaActionPerformed
        try{
            Producto pro = tienda.validarProducto( codProductoVenta.getText() );
            Producto aux = new Producto();
            if ( pro != null ){
                if ( pro.getCantidad() >= Integer.parseInt( cantidadProductoVenta.getText() ) ){
                    aux.setCantidad(Integer.parseInt( cantidadProductoVenta.getText() ) );
                    aux.setCodigo( pro.getCodigo() );
                    aux.setNombre( pro.getNombre() );
                    aux.setPrecio( pro.getPrecio() );
                    venta.add(aux);
                    tienda.ajustarCantidadProducto(pro.getCodigo(), aux.getCantidad() );
                    limpiarInputsVentaProductos();
                    listarVentasProductos();
                    listarProductos();
                }else{
                    JOptionPane.showMessageDialog(null, "Cantidad de producto requerida no está disponible", "Error", 
                    JOptionPane.WARNING_MESSAGE); 
                }
            }else{
               JOptionPane.showMessageDialog(null, "Producto no existe", "Error", 
                    JOptionPane.WARNING_MESSAGE); 
            }
        }catch( Exception e ){
            JOptionPane.showMessageDialog(null, "Ingrese los datos correctamente", "Error", 
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarProductoVentaActionPerformed

